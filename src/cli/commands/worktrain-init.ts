/**
 * WorkTrain Init Command
 *
 * Guided onboarding for WorkTrain daemon setup. Configures:
 *   1. LLM credentials (Bedrock or Anthropic) -- detection + print instructions
 *   2. Workspace path -- prompts user, writes WORKRAIL_DEFAULT_WORKSPACE to config.json
 *   3. SCM token (GitHub/GitLab) -- prompts user, prints export instructions, never writes to disk
 *   4. triggers.yml -- generates a starter file in the workspace if absent
 *   5. daemon-soul.md -- writes ~/.workrail/daemon-soul.md if absent
 *   6. Smoke test -- runs `workrail list` to verify installation
 *
 * Design invariants:
 * - All I/O is injected via WorktrainInitCommandDeps. Zero direct fs/readline imports.
 * - No secrets are ever written to disk. Token values stay in the user's shell profile.
 * - Each section is skip-if-already-configured (idempotent).
 * - All failures are returned as CliResult failure variants -- never thrown.
 * - The --yes flag skips all prompts and uses safe defaults (for CI / non-TTY use).
 */

import type { CliResult } from '../types/cli-result.js';
import { success, failure } from '../types/cli-result.js';
import { DAEMON_SOUL_TEMPLATE } from '../../daemon/soul-template.js';

// ═══════════════════════════════════════════════════════════════════════════
// DEPENDENCIES
// ═══════════════════════════════════════════════════════════════════════════

/**
 * All I/O operations required by the init command.
 * Inject real implementations in the composition root; inject fakes in tests.
 */
export interface WorktrainInitCommandDeps {
  /** Interactive prompt. Returns defaultValue when --yes is set (see opts.yes). */
  readonly prompt: (question: string, defaultValue?: string) => Promise<string>;
  /** Create directory (recursive: true = mkdir -p). */
  readonly mkdir: (path: string, options: { recursive: boolean }) => Promise<string | undefined>;
  /** Read file contents as UTF-8 string. Throws on ENOENT. */
  readonly readFile: (path: string) => Promise<string>;
  /** Write UTF-8 string to file, creating parent directory first. */
  readonly writeFile: (path: string, content: string) => Promise<void>;
  /** Return true if the path exists (file or directory). */
  readonly exists: (path: string) => Promise<boolean>;
  /** Return the user's home directory. */
  readonly homedir: () => string;
  /** Return the current working directory. Used as the default workspace in --yes mode. */
  readonly cwd: () => string;
  /** Join path segments (same semantics as node:path join). */
  readonly joinPath: (...paths: string[]) => string;
  /** Run the workrail binary and return stdout + success flag. */
  readonly runSmoke: () => Promise<{ readonly ok: boolean; readonly output: string }>;
  /** Print a line to stdout. */
  readonly print: (line: string) => void;
  /** Current process environment. Used to detect existing credentials. */
  readonly env: Readonly<Record<string, string | undefined>>;
}

/**
 * Options for the init command.
 */
export interface WorktrainInitCommandOpts {
  /**
   * When true, skip all interactive prompts and use safe defaults.
   * Intended for CI / non-TTY environments.
   */
  readonly yes?: boolean;
}

